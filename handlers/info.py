from keyboards.menu import main_menu_kb


router = Router()


@router.message(F.text == "О нас")
async def info(message: types.Message):
        text = (
            "Что можно хранить в наших боксах — примеры и советы.\n\n"
            "1. Снегоход / мототехника\n"
            "Безопасное долгосрочное хранение техники в сухом, охраняемом боксе.\n"
            "Рекомендация по размеру: от 6–10 м² в зависимости от габаритов.\n\n"
            "2. Лыжи, сноуборды и сезонное снаряжение\n"
            "Храните сезонные вещи без загромождения дома — быстрый доступ перед сезоном.\n"
            "Рекомендация по размеру: 1–3 м² для пары комплектов + сумок.\n\n"
            "3. Вещи на время переезда\n"
            "Краткосрочное и среднесрочное хранение при переезде — гибкие сроки, помесячная оплата.\n"
            "Рекомендация по размеру: от 1 м³ (коробки) до 6–8 м² для мебели.\n\n"
            "4. Крупная мебель и техника\n"
            "Сохранность мебели, мебели на реставрацию или временно не нужной техники.\n"
            "Рекомендация по размеру: 4–12 м² в зависимости от набора предметов.\n\n"
            "5. Хранение «на память»\n"
            "Семейные реликвии, документы, коллекции — под охраной и с контролем доступа.\n"
            "Рекомендация по размеру: 0.5–2 м² / ячейки.\n\n"
            "Ценность для клиента:\n"
            "1. Хранение сезонных и объёмных вещей — удобно и безопасно.\n"
            "2. Решение на время переезда — гибкие сроки и доступность.\n"
            "3. Освобождение пространства дома при сохранении ценных вещей.\n\n"
            "Ценность для владельца приложения:\n"
            "1. Дополнительный доход за счёт использования свободных секций.\n"
            "2. Быстрый MVP‑канал продаж для проверки спроса и сбора аудитории.\n\n"
        )

        await message.answer(text, reply_markup=main_menu_kb())